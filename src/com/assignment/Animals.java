package com.assignment;

//animal class: name, birthday, color, species
class Animal {
    String name;
    String birthday;
    String color;
    String species;

    //animal constructor. initializes animal object with the required fields
    Animal(String name, String birthday, String color, String species){
        this.name = name;
        this.birthday = birthday;
        this.color = color;
        this.species = species;
    }
}

